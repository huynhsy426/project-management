<script setup>
import projectService from "@/services/projectService";
import { ref, onMounted } from "vue";
import { useRoute, useRouter } from "vue-router";

const route = useRoute();
const router = useRouter();

let loading = ref(true);
let errMessage = ref("");
const projectId = route.params.projectId;

const taskAssign = ref({
  page: 1,
  ITEMS_PER_PAGE: 5,
  taskType: "a",
});
const taskNotAssign = ref({
  page: 1,
  ITEMS_PER_PAGE: 5,
  taskType: "na",
});

onMounted(async () => {
  try {
    const getProjectTaskAssign = await projectService.getTasks(
      projectId,
      taskAssign
    );

    const getProjectTaskNotAssign = await projectService.getTasks(
      projectId,
      taskNotAssign
    );

    loading.value = false;
    taskAssign.value.taskValue = {
      tasks: getProjectTaskAssign.tasks.tasks,
      pagination: getProjectTaskAssign.tasks.pagination,
    };
    taskNotAssign.value.taskValue = {
      tasks: getProjectTaskNotAssign.tasks.tasks,
      pagination: getProjectTaskNotAssign.tasks.pagination,
    };

    errMessage.value = "";
  } catch (error) {
    console.log(error);
    if (error?.status) {
      errMessage.value = error.data.messageCode;
    }
  }
});

const pagination = async (e, page, taskList) => {
  e.preventDefault();
  try {
    const listTask = await projectService.getTasks(projectId, {
      value: {
        page: page,
        ITEMS_PER_PAGE: taskList.ITEMS_PER_PAGE,
        taskType: taskList.taskType,
      },
    });

    taskList.page = page;
    taskList.taskValue = {
      tasks: listTask.tasks.tasks,
      pagination: listTask.tasks.pagination,
    };
  } catch (error) {
    console.log(error);
    if (error?.status) {
      errMessage.value = error.data.messageCode;
    }
  }
};

const handlePrevious = (e, tasks) => {
  if (tasks.page === 1) {
    return tasks.page;
  }
  pagination(e, tasks.page - 1, tasks);
};

const handleNext = (e, tasks) => {
  if (tasks.page === tasks.taskValue.pagination.pageCount) {
    return tasks.page;
  }
  pagination(e, tasks.page + 1, tasks);
};

const handleDetailTask = (taskId) => {
  router.push({ path: `/tasks/${taskId}` });
};

const handleCreateTask = () => {
  router.push({ path: `/tasks/create` });
};
</script>

